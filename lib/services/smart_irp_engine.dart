class SmartIrpPlan {
  final String smartGoals;
  final String clinicalPrecautions;
  final List<String> dailyActivities;

  SmartIrpPlan({
    required this.smartGoals,
    required this.clinicalPrecautions,
    required this.dailyActivities,
  });
}

class SmartIrpEngine {
  /// Головний метод генерації ІРП на основі клінічних даних
  static SmartIrpPlan generate({
    required String icdCode,
    required String diagnosis,
    required int treatmentDays,
    String? complaints,
    String? expectations,
  }) {
    final cleanCode = icdCode.trim().toUpperCase();
    final cleanComplaints = (complaints ?? '').toLowerCase();
    final cleanDiagnosis = diagnosis.toLowerCase();

    // 1. ВИЗНАЧЕННЯ ПРОФІЛЮ ПАТОЛОГІЇ
    bool isNeuro = cleanCode.startsWith('G') || cleanCode.startsWith('I6');
    bool isOrtho = cleanCode.startsWith('M') || cleanCode.startsWith('S') || cleanCode.startsWith('Z96') || cleanCode.startsWith('Z89');
    bool isCardioLung = cleanCode.startsWith('I2') || cleanCode.startsWith('I5') || cleanCode.startsWith('J');
    
    // Потужний чекер коморбідності (супутніх важких станів)
    bool hasRenalFailure = cleanCode.startsWith('N18') || cleanCode.startsWith('N19') || cleanDiagnosis.contains('нирк') || cleanComplaints.contains('нирк');
    bool hasMultiorganFailure = cleanCode.contains('R68.8') || cleanDiagnosis.contains('поліорган') || cleanDiagnosis.contains('сепсис');
    bool hasSeverePain = cleanComplaints.contains('біль') || cleanComplaints.contains('болить') || cleanCode.contains('R52');

    // 2. ГЕНЕРАЦІЯ КЛІНІЧНИХ ЗАСТЕРЕЖЕНЬ (Critical Precautions)
    String precautions = "• Стандартний моніторинг ЧСС, АТ, SpO2 перед та після навантаження.\n";
    if (hasMultiorganFailure) {
      precautions += "• ⚠️ КРИТИЧНО: Пацієнт у стані поліорганної недостатності. Навантаження виключно пасивне або в межах ліжка (ліжкова вертикалізація). Заборонено перевтому!\n";
    } else if (hasRenalFailure) {
      precautions += "• ⚠️ УВАГА: Наявність ниркової недостатності. Ризик затримки рідини, набряків та електролітного дисбалансу. Моніторинг ваги пацієнта та суворий контроль втомлюваності (Шкала Борга не вище 11-12 балів).\n";
    }
    if (isNeuro) {
      precautions += "• Контроль ортостатичної гіпотензії при вертикалізації. Профілактика синклінізій та спастичних контрактур.\n";
    }
    if (isOrtho) {
      precautions += "• Дотримання осьового навантаження на кінцівку згідно з хірургічним протоколом. Захист трансплантата/ендопротеза.\n";
    }

    // 3. ГЕНЕРАЦІЯ SMART-ЦІЛЕЙ
    String goals = "";
    String timeBound = "$treatmentDays днів";
    
    if (hasMultiorganFailure) {
      goals = "S (Специфічна): Профілактика гіпостатичних ускладнень та іммобілізаційного синдрому.\n"
              "M (Вимірювана): Збереження об'єму пасивних рухів у великих сугловах на початковому рівні (кут у градусах).\n"
              "A (Досяжна): Досяжно шляхом пасивної суглобової гімнастики та позиційного лікування.\n"
              "R (Релевантна): Важливо для збереження трофіки тканин до стабілізації соматичного стану.\n"
              "T (Обмежена в часі): Протягом курсу тривалістю $timeBound.";
    } else if (isNeuro) {
      String specificGoal = cleanComplaints.contains('ход') ? "Покращення статокінетичного балансу та відновлення патерну ходи" : "Зменшення спастичності та збільшення активної амплітуди рухів у паретичній кінцівці";
      goals = "S: $specificGoal.\n"
              "M: Збільшення показника за Шкалою Берга на 4-5 балів, зниження спастичності за модифікованою шкалою Ешворта на 1 бал.\n"
              "A: Реалістично за рахунок нейрофасилітації (PNF, Бобат-терапія) та тренувань на баланс-платформі.\n"
              "R: Спрямовано на підвищення рівня самообслуговування пацієнта та зменшення залежності від сторонніх.\n"
              "T: Термін реалізації — $timeBound.";
    } else if (isOrtho) {
      goals = "S: Збільшення амплітуди згинання/розгинання в цільовому суглобі та відновлення сили м'язів.\n"
              "M: Досягнення кута згинання згідно з нормативом гоніометрії (+15-20 градусів від початкового), зменшення болю за Шкалою ВАШ до рівня <= 3 балів.\n"
              "A: Забезпечується пасивно-активною мобілізацією, м'якими техніками мануальної терапії та ідеомоторними вправами.\n"
              "R: Необхідно для відновлення опороздатності кінцівки та самостійного пересування.\n"
              "T: Контрольна точка — $timeBound.";
    } else if (isCardioLung) {
      goals = "S: Підвищення толерантності до фізичних навантажень та покращення дренажної функції легень.\n"
              "M: Збільшення дистанції у Тесті 6-хвилинної ходьби на 50 метрів, зниження задишки за шкалою mMRC на 1 бал.\n"
              "A: Досягається шляхом дозованої ходьби, дихальної гімнастики з опором на видиху та релаксаційних технік.\n"
              "R: Зниження кардіореспіраторного дефіциту під час виконання повсякденних побутових завдань.\n"
              "T: Оцінка результату через $timeBound.";
    } else {
      goals = "S: Загальнофізичне відновлення організму, покращення м'язового тонусу та загальної рухливості.\n"
              "M: Збільшення індексу мобільності (наприклад, за шкалою Rivermead) на 2-3 пункти.\n"
              "A: Досяжно через індивідуально підібраний комплекс ЛФК та кінезіотерапії.\n"
              "R: Пов'язано із покращенням якості життя та загального функціонального статусу пацієнта.\n"
              "T: Виконання протоколу за $timeBound.";
    }

    if (expectations != null && expectations.isNotEmpty) {
      goals += "\n\n🎯 Враховано очікування пацієнта: \"$expectations\"";
    }

    // 4. ПОКРОКОВИЙ ПЛАН ПО ДНЯХ (РОЗПОДІЛ НА ФАЗИ)
    List<String> daysPlan = [];
    
    int phase1End = (treatmentDays * 0.3).round(); // Перші 30% часу - Адаптація
    int phase2End = (treatmentDays * 0.8).round(); // Наступні 50% часу - Інтенсив

    for (int day = 1; day <= treatmentDays; day++) {
      String dailyDescription = "";

      if (hasMultiorganFailure) {
        // Особливий суворий протокол для пацієнтів у критичному стані
        if (day <= phase1End) {
          dailyDescription = "Фаза І: Щадна пасивна. Позиційне лікування (антидекубітальний протокол) кожні 2 години. Пасивні рухи у дистальних суглобах кінцівок (10-15 повторень). Легкий дренажний масаж грудної клітки. Постійний моніторинг реанімаційних показників.";
        } else {
          dailyDescription = "Фаза ІІ: Стабілізаційна. Дихальні вправи (пасивне спонукання до глибокого видиху). Пасивна мобілізація всіх великих суглобів для запобігання тугорухливості. Ліжкова вертикалізація головного кінця ліжка до 30-45 градусів (контроль АТ!).";
        }
      } else if (isNeuro) {
        // Неврологічний протокол
        if (day <= phase1End) {
          dailyDescription = "Фаза І: Адаптаційна. Лікування положенням (вирівнювання та симетризація тіла). Пасивні та пасивно-активні вправи за методом Бобат. Вправи для стимуляції глибокої чутливості. Вертикалізація на поворотному столі (за потреби) або присаджування в ліжку з підтримкою. Дренажні дихальні техніки.";
        } else if (day <= phase2End) {
          dailyDescription = "Фаза ІІ: Мобілізаційна (Інтенсивна). Активно-пасивна гімнастика з використанням дзеркальної терапії. Проприоцептивна нейром'язова фасилітація (PNF). Тренування сидіння без підтримки. Перші спроби вставання біля опори. Масаж паретичних кінцівок (стимулюючий або розслабляючий залежно від тонусу).";
        } else {
          dailyDescription = "Фаза ІІІ: Функціональна. Тренування ходи в брусах або з ходунками. Вправи на координацію та баланс (вправи стоячи, перенесення ваги тіла). Ерготерапія: відновлення навичок самообслуговування, застібання ґудзиків, тримання ложки. Ергономіка ходи.";
        }
      } else if (isOrtho) {
        // Ортопедичний протокол
        if (day <= phase1End) {
          dailyDescription = "Фаза І: Протибольова та протинабрякова. Ізометрична гімнастика для м'язів навколо оперованого/травмованого суглоба. Пасивні рухи на Кinetec (CPM-терапія) у межах безболісної амплітуди. Кріотерапія локально (10-15 хв) після занять для зменшення набряку. Навчання правилам безпечного переміщення.";
        } else if (day <= phase2End) {
          dailyDescription = "Фаза ІІ: Відновлення амплітуди рухів (ROM). Активні вправи з розвантаженням кінцівки (блокові системи, підвісна терапія). М'які мануальні техніки мобілізації суглоба. Стретчинг спазмованих м'язових груп. Тренування ходи на ми милицях із чітким дозованим навантаженням на стопу (% від ваги тіла).";
        } else {
          dailyDescription = "Фаза ІІІ: Силова та пропріоцептивна. Вправи з легким опором (еластичні стрічки Thera-Band). Тренування балансу на балансувальних подушках для відновлення суглобового чуття (пропріоцепції). Корекція стереотипу ходи без допоміжних засобів (або з тростиною). Клінічний гоніометричний контроль.";
        }
      } else {
        // Загальномедичний / Кардіо-респіраторний протокол
        if (day <= phase1End) {
          dailyDescription = "Фаза І: Щадно-тренувальна. Дихальна гімнастика (діафрагмальне дихання, контроль темпу видиху). Дозовані ліжкові або приліжкові активні вправи для дрібних м'язових груп. Контроль задишки за шкалою Борга (тримаємо в межах легкого навантаження). Періоди відпочинку між вправами.";
        } else if (day <= phase2End) {
          dailyDescription = "Фаза ІІ: Збільшення толерантності. Поєднання дихальних вправ із динамічними вправами для рук та ніг сидячи на стільці. Дозована ходьба по коридору під контролем пульсоксиметрії (SpO2 не має падати нижче 92%). Вправи на релаксацію.";
        } else {
          dailyDescription = "Фаза ІІІ: Інтеграційна. Навчання підйому по сходах (ергономіка дихання при підйомі). Вправи на загальну витривалість тривалістю до 20-25 хвилин. Складання домашньої програми вправ та інструктаж родичів.";
        }
      }

      // Додаємо чергування фізіотерапевтичних методів або масажу по парних днях
      if (day % 2 == 0 && !hasMultiorganFailure) {
        if (isOrtho) dailyDescription += " Локально: Магнітотерапія або ультразвукова терапія для покращення консолідації тканин.";
        if (isNeuro) dailyDescription += " Додатково: Електростимуляція паретичних м'язів або розслабляючий масаж.";
      }

      daysPlan.add("День $day — $dailyDescription");
    }

    return SmartIrpPlan(
      smartGoals: goals,
      clinicalPrecautions: precautions,
      dailyActivities: daysPlan,
    );
  }
}

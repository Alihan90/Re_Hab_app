  /// Інтерпретація результатів шкали Берга
  static String interpretBerg(int score) {
    if (score >= 41) return "($score балів) — Низький ризик падіння. Пацієнт відносно незалежний у руховій активності.";
    if (score >= 21) return "($score балів) — Середній ризик падіння. Потребує допоміжних засобів (палиця, ходунки) та нагляду фізіотерапевта.";
    return "($score балів) — Високий ризик падіння! Пацієнт потребує постійної допомоги та суворого контролю безпеки.";
  }

  /// Інтерпретація результатів індексу Бартел
  static String interpretBarthel(int score) {
    if (score >= 91) return "($score балів) — Повна незалежність у повсякденному житті.";
    if (score >= 61) return "($score балів) — Помірна залежність. Потребує допомоги у складних побутових маніпуляціях.";
    if (score >= 21) return "($score балів) — Виражена залежність від сторонньої допомоги.";
    return "($score балів) — Повна залежність від оточуючих (необхідний постійний догляд).";
  }
